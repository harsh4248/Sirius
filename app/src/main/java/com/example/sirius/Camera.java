    private boolean allPermissionsGranted(){

       for(int i=0;i<REQUIRED_PERMISSIONS.length;i++) {
            if(ContextCompat.checkSelfPermission(this,REQUIRED_PERMISSIONS[i]) != PackageManager.PERMISSION_GRANTED) {

                return false;
            }

        }
        return true;


    }
